package igs.swnm.equiment;

public class HwRelay {

	private String relId;
	private String swcId;
	private int relNm;
	private int type;

	public HwRelay() {

	}

	public String getRelId() {
		return relId;
	}

	public void setRelId(String relId) {
		this.relId = relId;
	}

	public String getSwcId() {
		return swcId;
	}

	public void setSwcId(String swcId) {
		this.swcId = swcId;
	}

	public int getRelNm() {
		return relNm;
	}

	public void setRelNm(int relNm) {
		this.relNm = relNm;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

}
